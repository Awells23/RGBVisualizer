package com.example.rgbvisualizerlinear;

public class calculator {
        int red;
        int blue;
        int green;
    public calculator(){
       int red;
       int blue;
       int green;
    }
    //getter and setter methods for seekbars and displaying colors
    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }

}
